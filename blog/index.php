                    <div class="caption">Posted by Kim B. on 6th October, 2014</div>
                    <p>Before the Internet, small businesses only had a few ways to market their products cheaply, through methods like printing out fliers or sponsoring little local events. Now there are all kinds of opportunities out there on the Web — you just need to know where to look.</p>

<p>Here are six ways to promote your business online:</p>

<div class="section-heading">1. Use the three big local listing services</div>
<br>
<img src="img/blog3-googleplace.jpg">
<div class="caption">Google has provided a service for businesses to locate themselves in Google Map.</div>

<p>Registering your business with <a href="http://www.google.com/local/add/analyticsSplashPage">Google Places</a> allows it to be found more easily on Google search and Google Maps. All you have to do is fill out the form and register, then get your business verified through their confirmation process which can be done either with a phone call or snail mail. Yahoo also has a big database of businesses called <a href="https://local.yahoo.com/">Yahoo Local</a>. It's free, and is certainly worth the few minutes it takes to set up. Microsoft's <a href="http://www.bing.com/maps/">Bing</a> has a similar service that's easy to sign up for.</p>

<div class="section-heading">2. Embrace social media</div>

<p>Social media isn't just a tool to gain exposure — it has now become a necessary investment for every business to make. You can tie in ads and offers on your Facebook page and have a direct channel with your customers on Twitter. Networking on LinkedIn—both at the personal and company level—can be another way to help your startup.</p>

<img src="img/blog3-social.jpg">
<div class="caption">Social media can be a necessary time investment for businesses to make. Facing the expansion of social media, invest smart.</div>
<br>
<div class="section-heading">3. Start a blog</div>

<p>A blog not only helps your company get its name out through followers, but also a way to connect with your consumers more directly. Remember that one of the major keys of blogging is to keep your stream updated as frequently as you can. A dormant, abandoned blog is worth nothing.</p>

<div class="section-heading">4. Put up multimedia on YouTube and Flickr</div>

<p>YouTube provides a free way to distribute creative promotional videos, but in order to succeed you must put up contents that people want to view and are relevant to your business—a simple ad will not work. A Flickr profile can also help by giving you one place to compile all the photos for your business and allows you to link back to your website.</p>

<div class="section-heading">5. SEO your company website</div>

<p>Search engine optimization cannot be underestimated in the world of constant "Googling". Pick up a book or head over to an online how-to-guide on SEO and make sure your site is primed for performance on search engines.</p>

<div class="section-heading" style="line-height:90%;">6. Join and contribute to a relevant online community</div>

<p>Every niche has communities online that you can get involved in. But just signing up for a forum and posting every once in a while about your business isn't beneficial for anyone, and will likely just annoy people. Actively contribute and build a rapport with the community while keeping your business out of it. Passively promote your business by putting a link in your signature or mentioning it only when the context is appropriate.</p>

